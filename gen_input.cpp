#include <bits/stdc++.h>

using namespace std;
const int NUM = 1000;

 
mt19937 _g(time(nullptr));

int randint(int a, int b) {
    int w = (_g() << 31LL) ^ _g(); 
    return a + w % (b - a + 1); 
}

int main() {
	ofstream fout;
	fout.open("input.txt");
	cout << NUM << endl;
	for (int i = 0; i < NUM; i++) {
		fout << randint(100, 1000) << " " << randint(100, 1000) << endl;
	}
	return 0;
}